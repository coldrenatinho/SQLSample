DROP DATABASE Aerodromo;
CREATE DATABASE IF NOT EXISTS Aerodromo;

USE Aerodromo;

CREATE TABLE IF NOT EXISTS UF(
    CodUF VARCHAR(3) PRIMARY KEY NOT NULL,
    Descricao VARCHAR(100)
);

CREATE TABLE IF NOT EXISTS Municipio(
    Municipio VARCHAR(100) PRIMARY KEY NOT NULL,
    CodUF VARCHAR(3)NOT NULL
);

CREATE TABLE IF NOT EXISTS Aerodromo(
    OACI VARCHAR(10) PRIMARY KEY NOT NULL,
    CIAD VARCHAR(10) NOT NULL UNIQUE,
    NomeAerodromo VARCHAR(100) NOT NULL,
    MunicipioAtendido VARCHAR(200) NOT NULL,
    Latitude char(100) NOT NULL,
    Logitude CHAR(100) NOT NULL
);

CREATE IF NOT EXISTS EmpresaAeria(
    CodEmpresa VARCAHR(3) PRIMARY KEY NOT NULL,
    Descricao VARCHAR(100)
);

CREATE IF NOT EXISTS VooOperrado(
  ID_Voo PRIMARY KEY NOT NULL AUTO_INCREMENT,
  Data DATE NOT NULL,
  ICAOOrigem VARCHAR(100) NOT NULL,
  ICAODestino VARCHAR(100) NOT NULL,
  Tarifa INT NOT NULL,
  AsentosComercializados INT NOT NULL
);


ALTER TABLE Municipio ADD CONSTRAINT FOREIGN KEY FK_UF_CodUF (CodUF) REFERENCES UF_CodUF (CodUF);
